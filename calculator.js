// Define the numbers and the operation here
let number1=prompt("Please enter first number");
let number2=prompt("Please enter second number");
const operation=prompt("Please enter operation");

// Write if-statements here

if (operation === '+') console.log(number1 + number2);
else if (operation === '-') console.log(number1 - number2);
else if (operation === '*') console.log(number1 * number2);
else if (operation === '/') console.log(number1 / number2);
else console.log("Error!!");